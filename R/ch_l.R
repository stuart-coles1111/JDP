#' Function for optimisation of spending allocation
#'
#' Function for optimisation of spending allocation
#'
#'
#' @param p strength vector
#'
#'
#' @returns strength info
#' @examples
#' ch_l(c(5 ,5, 5)
#'
#' @export
#'
ch_l <- function(p){
  y <- - strengths_l(c(p[1], p[2], 15- (p[1] + p[2])), c(p[1], p[2], 15- (p[1] + p[2])))[[3]][2]
  cat(y,fill=T)
  y
}
